public class Variable {
    public static void main(String[] args) {
        //introducing a named person. If data type is text then use String
        String firstname = "Princess"; // String datatype = contains text
        String lastname = "Zelda";
        //adding age - starts with int = age. int data type contains whole number
        int age = 17;
        //char is used for one character. char data type = one character
        //String is used for multiple characters
        char gender = 'F';
        // Boolean data type contains true or false
        Boolean isattending = true;
        //float double data type = contains decimal numbers
        float weight = 55.5f;
        System.out.println("Student First Name = " + firstname);
        System.out.println("Student Last Name = " + lastname);
        System.out.println("Student Age = " + age);
        System.out.println("Student Gender = " + gender);
        System.out.println("Is Student Attending Hyrule? = " + isattending);
        System.out.println("Student Weight = " + weight);
    }
}
